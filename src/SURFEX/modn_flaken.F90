SUBROUTINE INIT_NAM_FLAKEn

  REAL(KIND=JPRB) :: ZHOOK_HANDLE

  IF (LHOOK) CALL DR_HOOK('MODN_FLAKE_N:INIT_NAM_FLAKEN',0,ZHOOK_HANDLE)
  XTSTEP=XTSTEP_n
  XOUT_TSTEP=XOUT_TSTEP_n
  LSEDIMENTS=LSEDIMENTS_n
  CFLK_FLUX=CFLK_FLUX_n
  CFLK_ALB=CFLK_ALB_n
  CSNOW_FLK=CSNOW_FLK_n
  XICHCE=XICHCE_n
  LPRECIP=LPRECIP_n
  LPWEBB=LPWEBB_n
