  REAL(KIND=JPRB) :: ZHOOK_HANDLE

  IF (LHOOK) CALL DR_HOOK('MODN_FLAKE_N:UPDATE_NAM_FLAKEN',0,ZHOOK_HANDLE)
  XTSTEP_n = XOUT_TSTEP
  XOUT_TSTEP_n = XOUT_TSTEP
  LSEDIMENTS_n  =LSEDIMENTS
