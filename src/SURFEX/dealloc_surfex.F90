 CALL FLAKE_DEALLO
 CALL FLAKE_SBL_DEALLO
 CALL GR_BIOG_GARDEN_DEALLO
 CALL GR_BIOG_DEALLO
 CALL IDEAL_DEALLO
 CALL IO_BUFF_DEALLO
 CALL ISBA_CANOPY_DEALLO
 CALL ISBA_GRID_DEALLO
 CALL ISBA_DEALLO
 CALL OCEAN_GRID_DEALLO
 CALL OCEAN_DEALLO
 CALL OCEAN_REL_DEALLO
 CALL SEAFLUX_GRID_DEALLO
 CALL SEAFLUX_DEALLO
 CALL SEAFLUX_SBL_DEALLO
 CALL SLT_DEALLO
 CALL SURF_ATM_GRID_DEALLO
 CALL SURF_ATM_DEALLO
 CALL SURF_ATM_SSO_DEALLO
 CALL DATA_COVER_DEALLO
 CALL SV_DEALLO
 CALL SSO_CANOPY_DEALLO
 CALL TEB_CANOPY_DEALLO
 CALL TEB_GARDEN_DEALLO
 CALL TEB_GARDEN_PGD_DEALLO
 CALL TEB_GARDEN_PGD_EVOL_DEALLO
 CALL TEB_GARDEN_OPTIONS_DEALLO
 CALL TEB_GRID_DEALLO
 CALL TEB_DEALLO
 CALL TEB_OPTIONS_DEALLO
 CALL TEB_VEG_OPTIONS_DEALLO
 CALL BEM_DEALLO
 CALL BEM_OPTIONS_DEALLO
 CALL TEB_GREENROOF_OPTIONS_DEALLO
 CALL TEB_GREENROOF_PGD_DEALLO
 CALL TEB_GREENROOF_PGD_EVOL_DEALLO
 CALL TEB_GREENROOF_DEALLO
 CALL WATFLUX_GRID_DEALLO
 CALL WATFLUX_DEALLO
 CALL WATFLUX_SBL_DEALLO
 CALL DIAG_TRIP_DEALLO
 CALL TRIP_GRID_DEALLO
 CALL TRIP_DEALLO
IF (LHOOK) CALL DR_HOOK('DEALLOC_SURFEX',1,ZHOOK_HANDLE)
!
!--------------------------------------------------------------------------------------
!
END SUBROUTINE DEALLOC_SURFEX
