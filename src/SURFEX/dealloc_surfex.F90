USE MODD_TRIP_GRID_n,   ONLY : TRIP_GRID_DEALLO
USE MODD_TRIP_n,   ONLY : TRIP_DEALLO

USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK
USE PARKIND1  ,ONLY : JPRB
!
IMPLICIT NONE
!
!*      0.1    declarations of arguments
!
!
!*      0.2    declarations of local variables
!
!-------------------------------------------------------------------------------------
!
REAL(KIND=JPRB) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('DEALLOC_SURFEX',0,ZHOOK_HANDLE)
 CALL AGRI_DEALLO
 CALL CH_EMIS_FIELD_DEALLO
 CALL CH_EMIS_SNAP_DEALLO
 CALL CH_ISBA_DEALLO
 CALL CH_SEAFLUX_DEALLO
 CALL CH_SURF_DEALLO
 CALL CH_TEB_DEALLO
 CALL CH_WATFLUX_DEALLO
 CALL DATA_ISBA_DEALLO
 CALL DATA_SEAFLUX_DEALLO
 CALL DATA_TEB_GARDEN_DEALLO
 CALL DATA_TEB_GREENROOF_DEALLO
 CALL DATA_TEB_DEALLO
 CALL DATA_BEM_DEALLO
 CALL BLD_DESC_DEALLO
 CALL DIAG_EVAP_ISBA_DEALLO
 CALL DIAG_FLAKE_DEALLO
 CALL DIAG_IDEAL_DEALLO
 CALL DIAG_ISBA_DEALLO
 CALL DATA_TSZ0_DEALLO
 CALL DIAG_MISC_FLAKE_DEALLO
 CALL DIAG_MISC_ISBA_DEALLO
 CALL DIAG_MISC_TEB_DEALLO
 CALL DIAG_MISC_TEB_OPTIONS_DEALLO
 CALL DIAG_UTCI_TEB_DEALLO
 CALL DIAG_OCEAN_DEALLO
 CALL DIAG_SEAFLUX_DEALLO
 CALL DIAG_SURF_ATM_DEALLO
 CALL DIAG_TEB_DEALLO
 CALL DIAG_WATFLUX_DEALLO
