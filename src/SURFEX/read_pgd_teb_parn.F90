     YRECFM='D_WIN_SW_MAX'
     CALL READ_FIELD(YRECFM,DTB%XPAR_WIN_SW_MAX,HDIRIN)
  ENDIF
  !
  IF (DTB%LDATA_FOPEN) THEN
     ALLOCATE(DTB%XPAR_FOPEN(KDIM))
     YRECFM='D_FOPEN'
     CALL READ_FIELD(YRECFM,DTB%XPAR_FOPEN,HDIRIN)
  ENDIF
  !
  IF (DTB%LDATA_FVSUM) THEN
