  REAL(KIND=JPRB) :: ZHOOK_HANDLE

  IF (LHOOK) CALL DR_HOOK('MODN_TEB_N:INIT_NAM_DIAG_TEBN',0,ZHOOK_HANDLE)
  LPGD = LPGD_n
  LPGD_FIX = LPGD_FIX_n
  LSURF_MISC_BUDGET = LSURF_MISC_BUDGET_n
