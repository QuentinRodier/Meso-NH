 ELSEIF (NVT_FLGR>0) THEN
   IF(DTV%XPAR_VEGTYPE(JI,NVT_FLGR)>0. )  ZDATA_CE_NITRO(JI,2)= 5.56
 ENDIF
 IF (ZPROP_HVEG(JI)+ZPROP_LVEG(JI) .GT. 0.)                    &
    DTV%XPAR_CE_NITRO(JI,1) =  ( ZDATA_CE_NITRO(JI,1)*ZPROP_HVEG(JI)     &
